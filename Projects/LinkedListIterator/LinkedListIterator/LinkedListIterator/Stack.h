#pragma once
#include "SLList.h"

template <typename T>
class TStack
{
private:

	TSLList<T> List;

public:

	//-------------------------------------------------------------------------------------------
	bool IsEmpty() const
	{
		return List.IsEmpty();
	}

	//-------------------------------------------------------------------------------------------
	int GetSize() const
	{
		return List.GetSize();
	}

	//-------------------------------------------------------------------------------------------
	T& Top()
	{
		return List.GetHead();
	}

	//-------------------------------------------------------------------------------------------
	const T& Top() const
	{
		return List.GetHead();
	}

	//-------------------------------------------------------------------------------------------
	void Pop()
	{
		List.RemoveHead();
	}

	//-------------------------------------------------------------------------------------------
	void Push(const T& Data)
	{
		List.AddHead(Data);
	}

};