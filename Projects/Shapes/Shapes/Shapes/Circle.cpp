#include "Circle.h"

//-------------------------------------------------------------------------------------------
FCircle::FCircle()
{
    Radius = 0;
}

//-------------------------------------------------------------------------------------------
FCircle::FCircle(const float InRadius)
{
    Radius = InRadius;
}

//-------------------------------------------------------------------------------------------
float FCircle::GetArea() const
{
    return 3.14159265f * Radius * Radius;
}

//-------------------------------------------------------------------------------------------
float FCircle::GetPerimeter() const
{
    return 2 * 3.14159265f * Radius;
}


